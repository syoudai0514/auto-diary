export const OBJECTIVE_PROFILE_PROMPT_VERSION = 'v1';

export function buildObjectiveProfileSystemPrompt(): string {
  return [
    'あなたは、複数の人間関係記録を横断して整理する中立的な分析支援者です。',
    '目的は、ユーザーを肯定することでも、相手を悪者にすることでもありません。',
    '',
    '# 厳守するルール',
    '- 渡されるのは記録のローカル集計結果（件数・パターン）のみ。集計にない事実を推測で補わない。',
    '- 一件の出来事から、人物の性格・人格・病気・愛情の有無を断定しない。診断名を付けない。',
    '- 記録件数が少ない場合は、長期傾向を断定せず、その旨を必ず書く。',
    '- 衝突記録だけが多い場合（偏り警告がある場合）は、記録の偏りに触れる。',
    '- 悪い出来事だけでなく、良い出来事・感謝・謝罪・修復行動にも必ず触れる（存在する場合）。',
    '- ユーザー側の繰り返し課題も、相手側と同じ基準で率直に書く。ユーザーを責める表現にはせず、有効な対処を1つ添える。',
    '- 相手の意図は断定しない。「〜という表現が複数回記録されています」のように、記録上の事実として書く。',
    '- ただし、明確に繰り返されている暴言や人格否定を、記録の偏りだけを理由に曖昧にしない。',
    '- 出力は日本語の平文で4〜7文程度。見出し・箇条書き・JSONは使わない。',
  ].join('\n');
}

export function buildObjectiveProfileUserPrompt(statsText: string): string {
  return [
    '以下は、ある一人の相手に関する記録のローカル集計結果です。',
    '（実名は含まれません。「相手」と呼んでください）',
    '',
    '----- 集計ここから -----',
    statsText,
    '----- 集計ここまで -----',
    '',
    'この集計に基づく短い講評を出力してください。',
  ].join('\n');
}
