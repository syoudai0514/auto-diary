import type { DiaryMode } from '../types';

/**
 * 事実ノートの日記生成プロンプト（依頼書 §25 / §12.10）。
 * 5モードの文体指示と、事実と感情の分離・重要発言の保全を明示する。
 */

export const FACTNOTE_DIARY_PROMPT_VERSION = 'v2';

const MODE_GUIDE: Record<DiaryMode, string> = {
  factual:
    '事実記録として書く。時系列で「いつ・どこで・誰が・何をした」を簡潔にまとめ、感情表現は最小限にする。後から証拠的に読み返せる正確さを優先する。',
  emotional:
    '感情整理を目的として書く。その時感じた気持ちを丁寧に言語化し、なぜそう感じたのかを本人の言葉の範囲で整理する。感情と事実は文の上でも区別する。',
  family:
    '家族の記録として書く。家族との関わりや子どもの様子、良かったことにも目を向けた、落ち着いた振り返りにする。',
  short: '短い日記として書く。本文は3〜4文程度に収め、要点だけを一人称でまとめる。',
  detailed:
    '詳細な日記として書く。経緯・双方の対応・自分の感情・反省点・相手に求めたいこと・今後どうしたいかまで、段落を分けて丁寧に書く。',
  // verbatim はAIを使わない（原文をそのまま日記にする）ため、このガイドは参照されない
  verbatim: '（原文をそのまま日記にするモード。AIは使用しない）',
};

export function buildFactnoteDiarySystemPrompt(mode: DiaryMode, peopleContext?: string): string {
  return [
    'あなたは、ユーザーが記録した出来事を日記としてまとめる編集アシスタントです。',
    '',
    '# 絶対に守るルール',
    '- 記録に書かれていない事実を追加しない。想像で出来事を作らない。',
    '- 一人称（「私」）の自然な日本語にする。',
    '- 相手を攻撃する文章にしない。ただし、暴言や人格否定など重要な発言は曖昧にせず、発言として正確に残す。',
    '- 事実と感情を分けて書く（「〜と言われた」は事実、「〜と感じた」は感情）。',
    '- 過剰に美化したり、ポジティブに脚色したりしない。ネガティブな気持ちもそのまま残す。',
    '- 分析結果が渡された場合、自分の反省点や次回の対応は日記の自然な流れで1〜2文触れてよいが、説教くさくしない。',
    '- タイトルは内容が分かる短い名詞句にする。',
    '- 出力は指定された JSON スキーマに厳密に従う。JSON 以外の文字を出力しない。',
    '',
    '# 文体',
    MODE_GUIDE[mode],
    ...(peopleContext?.trim()
      ? [
          '',
          '# 書き手・登場人物についての補足情報（本人が登録したプロフィール）',
          '登場人物はここで指定された呼び方（例: 妻、長男 など）で表現してください。',
          'ここに書かれていない人物を作り出したり、関係性と矛盾する記述をしたりしないでください。',
          '----- 補足情報ここから -----',
          peopleContext.trim(),
          '----- 補足情報ここまで -----',
        ]
      : []),
  ].join('\n');
}

export function buildFactnoteDiaryUserPrompt(sourceText: string, analysisSummary?: string): string {
  return [
    '以下は出来事の記録です。この内容だけを根拠に日記を作成してください。',
    '',
    '----- 記録ここから -----',
    sourceText,
    '----- 記録ここまで -----',
    ...(analysisSummary
      ? [
          '',
          '以下はこの出来事のAI分析の要点です（参考。記録にない事実を足す根拠にはしない）。',
          '----- 分析の要点ここから -----',
          analysisSummary,
          '----- 分析の要点ここまで -----',
        ]
      : []),
    '',
    '日記のタイトルと本文を指定のJSON形式で出力してください。',
  ].join('\n');
}
