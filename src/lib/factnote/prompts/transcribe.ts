/**
 * 事実ノートの文字起こしプロンプト（依頼書 §24）。
 * `buildTalkTranscribePrompt` を土台に、原文の保全（美化・削除の禁止）と
 * `[聞き取れず]` の扱いを明示したもの。
 * プロフィール（peopleContext）がある場合は、A:/B: ではなく
 * 実際の呼び名（私:/妻: など）で話者ラベルを付けさせる。
 */

export const FACTNOTE_TRANSCRIBE_PROMPT_VERSION = 'v2';

export function buildFactnoteTranscribePrompt(peopleContext?: string): string {
  const speakerRules = peopleContext?.trim()
    ? [
        '- 複数の人物の会話の場合は、声の特徴と下記の補足情報から話者を推定し、各発言の先頭に補足情報の呼び方でラベルを付ける（例: 「私: 」「妻: 」「長男: 」）。記録を書いている本人は「私: 」とする。',
        '- 補足情報から判断できない話者は「A: 」「B: 」（子どもと思われる声は「C: 」）でラベルを付け、推測で名前を割り当てない。',
      ]
    : [
        '- 複数の人物の会話の場合は、声の特徴から話者を推定し、各発言の先頭に「A: 」「B: 」を付ける（最初に発言した人物をA）。子どもと思われる声は「C: 」とする。1人の独白の場合はラベル不要。',
      ];

  return [
    '添付された音声を、日本語として一字一句正確に文字起こししてください。',
    '',
    '# 厳守するルール',
    '- 原文を美化・言い換え・要約しない。強い言葉や暴言もそのまま書き起こす（この記録の目的は事実の保全です）。',
    '- 内容を追加・省略しない。存在しない発言を作らない。',
    '- 聞き取れない部分は推測で補わず「[聞き取れず]」と書く。',
    ...speakerRules,
    '- 話者が判別できない発言は「?: 」を付け、断定しない。',
    '- タイムスタンプは出力しない。',
    '- 文字起こしテキストのみを出力し、説明や前置きは付けない。',
    '- 音声に発話が含まれない場合は、何も出力しない。',
    ...(peopleContext?.trim()
      ? [
          '',
          '# 話者についての補足情報（記録者本人が登録したプロフィール）',
          '----- 補足情報ここから -----',
          peopleContext.trim(),
          '----- 補足情報ここまで -----',
        ]
      : []),
  ].join('\n');
}
