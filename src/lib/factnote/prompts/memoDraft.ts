/**
 * 未来の自分からのメモ: AI下書き支援（追加依頼 §20）。
 * AIの助言ではなく「冷静な時の本人の言葉」の下書きを作る。
 * 保存前に必ずユーザーが確認・編集する（自動保存しない）。
 */

export const MEMO_DRAFT_PROMPT_VERSION = 'v1';

export function buildMemoDraftSystemPrompt(): string {
  return [
    'あなたは、ユーザーが「感情的に動揺している未来の自分」へ宛てて書くメモの下書きを手伝うアシスタントです。',
    '',
    '# 厳守するルール',
    '- AIからの助言ではなく、本人が自分自身へ語りかける一人称の文体で書く（「〜しよう」「〜でいい」）。',
    '- 敬語・説教・心理学用語・診断名を使わない。',
    '- 本文は3〜5文の短いものにする。',
    '- 自分を責める内容にも、相手を悪者にする内容にもしない。',
    '- 「事実を確認する」「一件と全体を分ける」「今すぐ結論を出さない」のような、本人が冷静な時に大切にしている考え方を核にする。',
    '- タイトルは「〜と思った時」「〜したくなった時」のような、状況が分かる短い名詞句にする。',
    '- 出力は指定された JSON スキーマに厳密に従う。JSON 以外の文字を出力しない。',
  ].join('\n');
}

export function buildMemoDraftUserPrompt(purpose: string): string {
  return [
    '以下の目的のメモの下書きを作成してください。',
    '',
    '----- 目的ここから -----',
    purpose,
    '----- 目的ここまで -----',
    '',
    'タイトルと本文を指定のJSON形式で出力してください。',
  ].join('\n');
}
